#operadores matematicos
valor1 = 10
valor2 = 20

print(valor1 + valor2)
print(valor1 - valor2)
print(valor1 * valor2)
print(valor2 / valor1)
print(valor2 // valor1)

#operadores de comparação
saldo = 450
saque = 200

print(saldo == saque)
#retorna false
print(saque != saldo)
#retorna true
print(saque >= saldo)
#retorna false
print(saque < saldo)
#retorna true

saldo2 = 200
saque2 = 200

print(saldo2 == saque2)
print(saldo2 != saque2)
print(saldo2 > saque2)
print(saldo2 >= saque2)
print(saldo2 < saque2)
print(saldo2 <= saque2)

#operadores de atribuição
#atribuição simples
cod = 500
print(cod)

#atribuição com adição e subtração
cod += 200
print(cod)

cod -= 100
print(cod)

#com divisão
cod /= 100 #divisão ponto flutuante
print(cod)

cod //= 100 #divisao inteira
print(cod)

#com modulo
cod %= 100
print(cod)

#com exponenciação
cod **= 2
print(cod)